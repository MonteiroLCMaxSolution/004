		<h2 class="title text-center">Politicas de Privacidade</h2>
		<div class="row">
			A 3Ecomercial tem o compromisso com a transparência, a privacidade e a segurança dos dados de seus clientes durante todo o processo de interação com nosso site, aplicativo e  físicas. Para que entendam melhor quais informações coletamos e como as utilizamos, armazenamos ou excluímos, detalhamos a seguir nossa Política de Privacidade.<BR/>

Os dados cadastrais dos clientes não são divulgados para terceiros, exceto quando necessários para o processo de entrega, para cobrança ou participação em promoções solicitadas pelos clientes. Seus dados pessoais são peça fundamental para que o pedido chegue em segurança na sua casa, de acordo com o prazo de entrega estipulado.<BR/><BR/>

<p><strong>A fim de facilitar o entendimento da nossa Política de Privacidade, apresentamos um resumo:</strong></p>

Agente de tratamento dos dados: 3Ecomercial<BR/>

Papel da 3Ecomercial no tratamento dos dados: Predominantemente controlador<BR/>

Tipo de informações pessoais coletadas: A 3Ecomercial coleta as informações que o Cliente fornece quando se cadastra e cria uma conta em “Minha Conta”, em nosso site e aplicativo, e/ou quando faz uma compra ou troca produtos nas  físicas.<BR/><BR/>

<p><strong>Com que finalidade utilizamos seus dados pessoais?</strong></p>

Para entregar os produtos ou serviços adquiridos;<BR/>
Para mantê-lo informado sobre o status do seu pedido;<BR/>
Para coordenar com parceiros a entrega ou disponibilização de serviços;<BR/>
Para a realização de trocas;<BR/>
Para enviar comunicações sobre serviços e proporcionar uma melhor experiência ao Cliente, fazendo com que a 3Ecomercial seja ainda mais relevante em seu dia a dia.<BR/>
Seus direitos<BR/>

<p>A Lei Geral de Proteção de Dados (LGPD) dá ao Cliente o direito de solicitar a 3Ecomercial, a qualquer momento, mediante requisição formal, informações referentes aos dados tratados.</p>

<p><strong>Para que a Política de Privacidade seja bem compreendida, é fundamental esclarecer alguns conceitos importantes:</strong></p>

Cliente – toda pessoa física que adquire produtos ou serviços nas  físicas, site e aplicativo;<BR/>
Dados pessoais – qualquer informação relacionada a uma pessoa que a identifique ou que, usada em combinação com outras informações tratadas, identifique um indivíduo. Ainda, qualquer informação por meio da qual seja possível identificar uma pessoa ou entrar em contato com ela.<BR/>
Tratamento de dados pessoais – considera-se tratamento de dado pessoal a coleta, produção, recepção, classificação, utilização, acesso, reprodução, transmissão, distribuição, processamento, arquivamento, armazenamento, eliminação, avaliação ou controle da informação, comunicação, transferência, difusão ou extração de dados de pessoas físicas.<BR/>
Titular dados – qualquer pessoa física que tenha seus dados pessoais tratados pela 3Ecomercial;<BR/>
Finalidade – o que queremos alcançar com o tratamento de dados pessoais.<BR/>
Necessidade – o tratamento de dados pessoais deve se limitar ao mínimo necessário para o propósito almejado. Ou seja, deve ser pertinente, proporcional e não excessivo.<BR/>
Consentimento – autorização clara e objetiva que o titular dá para tratamento de seus dados pessoais com finalidade previamente estipulada. Após dar o consentimento, você pode revogá-lo a qualquer momento. A revogação não cancela os processamentos realizados previamente.<BR/><BR/>

<p><strong>1. A quem essa política de privacidade se aplica?</strong></p>
Aplica-se a todos os clientes da 3Ecomercial, incluindo site, aplicativo e  físicas, que de alguma forma tenham os dados pessoais tratados por nós.<BR/><BR/>

<p><strong>2. Que tipo de informações pessoais coletamos e utilizamos?</strong></p>
A 3Ecomercial coleta e armazena os seguintes tipos de informações:<BR/>

Informações que você nos fornece: a 3Ecomercial coleta informações do Cliente de diversas formas: quando você fornece seu CPF para emissão da nota fiscal; troca algum produto; adquire um dos serviços oferecidos pelas  físicas, site ou aplicativo; cria uma conta e fornece dados no “Minha Conta”; faz um pedido em nosso site ou aplicativo; retira um produto que comprou pelo site ou aplicativo nas  físicas; participa do sorteio mensal; quando interage com o Serviço de Atendimento ao Consumidor; quando participa de pesquisas ou promoções de marketing etc. Dentre as informações que podem ser solicitadas estão: nome completo, e-mail, CPF, data de nascimento, gênero, endereço, números de telefone e número do cartão de crédito.<BR/>

Informações coletadas automaticamente pela 3Ecomercial: coletamos e armazenamos determinadas informações sempre que o Cliente interage conosco. Por exemplo, utilizamos cookies e obtemos informações quando seu navegador acessa o nosso site; quando você clica em anúncios, e-mails de parceiros e outros conteúdos fornecidos por nós em outros sites.<BR/>

Informações coletadas pelo dispositivo móvel: quando o Cliente baixa ou utiliza o nosso aplicativo, podemos receber dados sobre sua localização e seu dispositivo móvel. Podemos utilizar essas informações para oferecer conteúdos personalizados como, por exemplo, anúncios, resultados de buscas e outros. A maioria dos dispositivos móveis permite que o Cliente desligue os serviços de localização.<BR/>

Informações coletadas de outras fontes:<BR/>

fontes públicas (por exemplo, dados demográficos);<BR/>
agências ou órgãos de crédito (por exemplo, relatórios de crédito/cheques);<BR/>
provedores de dados (por exemplo, dados demográficos relacionados a anúncios publicitários on-line e com base nos interesses do Cliente).<BR/><BR/>
<p><strong>3. Por que solicitamos seus dados pessoais?</strong></p>
Os dados são necessários para:<BR/>

Entregar os produtos adquiridos ou o serviço contratado;<BR/>
otimizar sua interação conosco;<BR/>
garantir a segurança do site, de seu aplicativo e dos dados que processa;<BR/>
informar o Cliente sobre as ofertas e divulgar os nossos serviços;<BR/>
cumprir obrigações legais;<BR/>
fins administrativos e de gestão.<BR/>
O fornecimento dos dados permite:<BR/>

Entregar os produtos ou serviços adquiridos;<BR/>
realizar o processo de troca ou devolução de produtos, dentro do prazo estabelecido pelo CDC (Código de Defesa do Consumidor);<BR/>
enviar atualizações sobre o status do pedido e comunicações de serviços;<BR/>
coordenar com parceiros a entrega ou disponibilização de serviços;<BR/>
prestar serviços adequados às necessidades do Cliente;<BR/>
melhorar a experiência de compra dos clientes na 3Ecomercial;<BR/>
fazer análises e pesquisas de mercado;<BR/>
manter o Cliente informado sobre os produtos e serviços que oferecemos;<BR/>
executar publicidade online direcionada;<BR/>
prevenir, detectar e investigar atividades que estejam em desacordo com o nosso Código de Conduta ou que sejam proibidas ou ilegais; melhorar nosso website, produtos e serviços.<BR/>
A 3Ecomercial utiliza bases legais, que podem variar de acordo com a finalidade da coleta, para tratar os dados pessoais dos clientes. O prazo de armazenamento pode mudar conforme a base legal aplicável a cada situação e a finalidade.<BR/><BR/>

<p><strong>3.1 Serviços de marketing</strong></p>

Os serviços de marketing são oferecidos por meio de comunicações gratuitas sobre ofertas, serviços e produtos dirigidas ao Cliente, relacionadas ao seu perfil no site e a compras que fez. Esse serviço abrange e-mails, SMS e WhatsApp. Vale ressaltar que o Cliente pode cancelar o serviço, a qualquer momento, acessando “Minha Conta”.<BR/><BR/>

<p><strong>4. Com quem compartilhamos seus dados?</strong>/p>
As informações coletadas somente serão compartilhadas quando forem necessárias: (i) para prestação adequada dos serviços objeto de suas atividades com empresas parceiras; (ii) proteção em caso de conflito; (iii) mediante decisão judicial ou requisição de autoridade competente; (iv) com empresas provedoras de infraestrutura tecnológica e operacional, como empresas intermediadoras de pagamento e provedoras de serviço de armazenamento de informações.<BR/><BR/>

<p><strong>5. Transferência internacional de dados</strong></p>
Como a internet é um ambiente global, determinados serviços oferecidos pela 3Ecomercial podem demandar a transferência dos seus dados para outros países.<BR/>

Nesses casos, os dados são tratados de acordo com a LGPD (Lei Geral de Proteção de Dados) e demais legislações de proteção. Tomamos medidas de segurança de acordo com nossas políticas e adotamos cláusulas padrão nos contratos com fornecedores e prestadores de serviço.<BR/>

Ao navegar em nosso site, aplicativo, comprar em nossas  físicas e/ou se comunicar conosco, você concorda com o tratamento de suas informações, inclusive a transferência internacional de dados, quando necessário. Adotamos medidas para garantir que quaisquer informações coletadas sejam tratadas com segurança, conforme os padrões de proteção de dados e de acordo com esta Política de Privacidade.<BR/><BR/>

<p><strong>6. Por quanto tempo armazenamos informações pessoais?</strong></p>
Armazenamos as informações dos Clientes de acordo com as normas de prescrição do Direito brasileiro.<BR/><BR/>

<p><strong>7. Quais são os direitos do titular de dados?</strong></p>
A partir do início da vigência da LGPD, o titular dos dados pessoais terá o direito de obter da 3Ecomercial, a qualquer momento, mediante requisição formal, informações referentes aos seus dados.<BR/>

A 3Ecomercial terá 15 dias para responder às solicitações dos titulares. Os pedidos serão analisados conforme previsto em legislação vigente e, por questões legais, algumas solicitações podem não ser atendidas.<BR/>

Os titulares dos dados, segundo o texto da LGPD, podem exercer os seus direitos por meio de:<BR/>

I - confirmação da existência de tratamento;<BR/>

II - acesso aos dados;<BR/>

III - correção de dados incompletos, inexatos ou desatualizados;<BR/>

IV - anonimização, bloqueio ou eliminação de dados desnecessários, excessivos ou tratados em desconformidade com o disposto nesta Lei;<BR/>
V - portabilidade dos dados a outro fornecedor de serviço ou produto, mediante requisição expressa, de acordo com a regulamentação da autoridade nacional, observados os segredos comercial e industrial;<BR/>

VI - eliminação dos dados pessoais tratados com o consentimento do titular;<BR/>

VII - informação das entidades públicas e privadas com as quais o controlador realizou uso compartilhado de dados;<BR/>

VIII - informação sobre a possibilidade de não fornecer consentimento e sobre as consequências da negativa;<BR/>

IX - revogação do consentimento.<BR/><BR/>

<p><strong>8. Como exercer os seus direitos?</strong></p>
Você pode exercer seus direitos acessando a seção “Privacidade”, no “Minha Conta”, disponível no site www.3Ecomercial.com.br. Além disso, pode entrar em contato pelo e-mail atendimento.acom@3Ecomercial.com.<BR/>

Para mudar suas preferências em relação às notificações (incluindo as comunicações de marketing), a qualquer momento, você pode acessar “Minha Conta”. Se não quiser receber nossas comunicações de marketing, também pode cancelar a assinatura clicando no link enviado junto com o e-mail do marketing.<BR/><BR/>


<p><strong>9. crianças podem comprar na 3Ecomercial?</strong></p>
A 3Ecomercial vende produtos para crianças, mas devem ser comprados por adultos. Se você tem menos de 18 anos, precisará fazer a compra junto com um de seus pais ou responsável.<BR/><BR/>

