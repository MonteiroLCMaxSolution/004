<p><strong>11. como você pode manter suas informações pessoais seguras?</strong></p>
A 3Ecomercial utiliza os melhores protocolos de segurança para preservar a privacidade dos dados dos Clientes, mas também recomenda medidas de proteção individual.<BR/>

Para manter a segurança e proteção dos seus dados pessoais fornecidos no cadastro ou nas compras, em nenhuma hipótese o seu login e senha devem ser compartilhados com terceiros. Além disso, ao acessar o seu cadastro pelo “Minha Conta”, principalmente em computadores públicos, certifique-se de que você realizou o logout da sua conta para evitar que pessoas não autorizadas acessem e utilizem as informações sem o seu conhecimento.<BR/>

Não solicitamos dados pessoais por telefone, WhatsApp, SMS ou e-mail. Em nenhuma hipótese eles devem ser fornecidos, pois pode ser uma tentativa de uso indevido.<BR/>

Para mais informações, você pode consultar nosso Guia de segurança.<BR/><BR/>

